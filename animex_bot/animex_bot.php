/* ============================ 1) ذخیره‌سازی (SQLite) ====================== */
class AXStore {
    private SQLite3 $db;
    public function __construct() {
        $this->db = new SQLite3(AX_DB);
        $this->db->busyTimeout(5000);
        $this->db->exec('PRAGMA journal_mode=WAL');
        $this->db->exec('CREATE TABLE IF NOT EXISTS users(
            id INTEGER PRIMARY KEY, first_name TEXT, username TEXT,
            is_admin INTEGER DEFAULT 0, vip_until INTEGER DEFAULT 0,
            banned INTEGER DEFAULT 0, ban_reason TEXT,
            joined INTEGER, last_seen INTEGER, total_req INTEGER DEFAULT 0)');
        $this->db->exec('CREATE TABLE IF NOT EXISTS usage(
            user_id INTEGER, d TEXT, c INTEGER DEFAULT 0, PRIMARY KEY(user_id,d))');
        $this->db->exec('CREATE TABLE IF NOT EXISTS cache(
            hash TEXT PRIMARY KEY, url TEXT, data TEXT, created INTEGER)');
        $this->db->exec('CREATE TABLE IF NOT EXISTS logs(
            id INTEGER PRIMARY KEY AUTOINCREMENT, user_id INTEGER, action TEXT, url TEXT, ts INTEGER)');
    }
    public function ensure_user(int $id, string $first, string $uname): void {
        $q = $this->db->prepare('INSERT INTO users(id,first_name,username,joined,last_seen) VALUES(?,?,?,?,?)
            ON CONFLICT(id) DO UPDATE SET first_name=excluded.first_name, username=excluded.username, last_seen=excluded.last_seen');
        $now = time();
        $q->execute([$id, $first, $uname, $now, $now]);
    }
    public function get_user(int $id): ?array {
        $q = $this->db->prepare('SELECT * FROM users WHERE id=?');
        $r = $q->execute([$id]);
        $row = $r->fetchArray(SQLITE3_ASSOC);
        return $row ?: null;
    }
    public function set_admin(int $id, bool $v): void {
        $this->db->prepare('UPDATE users SET is_admin=? WHERE id=?')->execute([$v ? 1 : 0, $id]);
    }
    public function set_vip(int $id, int $days): void {
        $row = $this->get_user($id);
        $cur = $row ? (int)$row['vip_until'] : 0;
        $base = time() > $cur ? time() : $cur;
        $until = $base + $days * 86400;
        $this->db->prepare('UPDATE users SET vip_until=? WHERE id=?')->execute([$until, $id]);
    }
    public function ban(int $id, string $reason = ''): void {
        $this->db->prepare('UPDATE users SET banned=1, ban_reason=? WHERE id=?')->execute([$reason, $id]);
    }
    public function unban(int $id): void {
        $this->db->prepare('UPDATE users SET banned=0, ban_reason=NULL WHERE id=?')->execute([$id]);
    }
    public function usage_today(int $id): int {
        $r = $this->db->prepare('SELECT c FROM usage WHERE user_id=? AND d=?');
        $x = $r->execute([$id, date('Y-m-d')])->fetchArray(SQLITE3_ASSOC);
        return $x ? (int)$x['c'] : 0;
    }
    public function inc_usage(int $id): void {
        $this->db->prepare('INSERT INTO usage(user_id,d,c) VALUES(?,?,1)
            ON CONFLICT(user_id,d) DO UPDATE SET c=c+1')->execute([$id, date('Y-m-d')]);
        $this->db->prepare('UPDATE users SET total_req=total_req+1 WHERE id=?')->execute([$id]);
    }
    public function cache_get(string $hash): ?array {
        $r = $this->db->prepare('SELECT * FROM cache WHERE hash=?');
        $x = $r->execute([$hash])->fetchArray(SQLITE3_ASSOC);
        return $x ?: null;
    }
    public function cache_set(string $hash, string $url, string $data): void {
        $this->db->prepare('INSERT INTO cache(hash,url,data,created) VALUES(?,?,?,?)
            ON CONFLICT(hash) DO UPDATE SET url=excluded.url, data=excluded.data, created=excluded.created')
            ->execute([$hash, $url, $data, time()]);
    }
    public function log(int $uid, string $action, string $url = ''): void {
        $this->db->prepare('INSERT INTO logs(user_id,action,url,ts) VALUES(?,?,?,?)')->execute([$uid, $action, $url, time()]);
    }
    public function stats(): array {
        $q = fn(string $s) => (int)$this->db->querySingle($s);
        return [
            'users'    => $q('SELECT COUNT(*) FROM users'),
            'admins'   => $q('SELECT COUNT(*) FROM users WHERE is_admin=1'),
            'vip'      => $q('SELECT COUNT(*) FROM users WHERE vip_until>' . time()),
            'banned'   => $q('SELECT COUNT(*) FROM users WHERE banned=1'),
            'today'    => $q("SELECT COALESCE(SUM(c),0) FROM usage WHERE d='" . date('Y-m-d') . "'"),
            'total'    => $q('SELECT COALESCE(SUM(total_req),0) FROM users'),
            'cache'    => $q('SELECT COUNT(*) FROM cache'),
        ];
    }
    public function users_page(int $page = 0, int $per = 30): array {
        $off = $page * $per;
        $r = $this->db->query("SELECT * FROM users ORDER BY id DESC LIMIT $per OFFSET $off");
        $out = [];
        while ($row = $r->fetchArray(SQLITE3_ASSOC)) $out[] = $row;
        return $out;
    }
    public function all_ids(): array {
        $r = $this->db->query('SELECT id FROM users');
        $out = [];
        while ($row = $r->fetchArray(SQLITE3_ASSOC)) $out[] = (int)$row['id'];
        return $out;
    }
}

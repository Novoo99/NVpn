function ax_tg(string $method, array $params = []) {
    $cfg = ax_cfg(); if (!$cfg) return null;
    $url = 'https://api.telegram.org/bot' . $cfg['bot_token'] . '/' . $method;
    $ch = curl_init($url);
    curl_setopt_array($ch, [
        CURLOPT_RETURNTRANSFER => true,
        CURLOPT_POST           => true,
        CURLOPT_POSTFIELDS     => $params,
        CURLOPT_TIMEOUT        => 50,
        CURLOPT_SSL_VERIFYPEER => false,
    ]);
    $res = curl_exec($ch); curl_close($ch);
    return json_decode($res, true);
}
function ax_tg_upload(string $method, array $fields, array $files) {
    $cfg = ax_cfg(); if (!$cfg) return null;
    $boundary = uniqid('ax', true);
    $eol = "\r\n"; $body = '';
    foreach ($fields as $k => $v) {
        if ($v === null || $v === '') continue;
        $body .= "--$boundary$eolContent-Disposition: form-data; name=\"$k\"$eol$eol$v$eol";
    }
    foreach ($files as $name => $path) {
        $body .= "--$boundary$eolContent-Disposition: form-data; name=\"$name\"; filename=\"" . basename($path) . "\"$eol";
        $body .= "Content-Type: application/octet-stream$eol$eol" . file_get_contents($path) . $eol;
    }
    $body .= "--$boundary--$eol";
    $ch = curl_init('https://api.telegram.org/bot' . $cfg['bot_token'] . '/' . $method);
    curl_setopt_array($ch, [
        CURLOPT_RETURNTRANSFER => true, CURLOPT_POST => true, CURLOPT_POSTFIELDS => $body,
        CURLOPT_HTTPHEADER => ['Content-Type: multipart/form-data; boundary=' . $boundary],
        CURLOPT_TIMEOUT => 90, CURLOPT_SSL_VERIFYPEER => false,
    ]);
    $res = curl_exec($ch); curl_close($ch);
    return json_decode($res, true);
}

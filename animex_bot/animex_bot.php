function ax_scrape(string $url): array {
    $url = trim($url);
    $html = ax_http($url);
    if (!$html) return ['ok' => false, 'error' => 'دریافت صفحه ناموفق بود (احتمالاً سایت در دسترس نیست).'];
    // ساخت DOM با charset درست (ترفند xml encoding برای UTF-8 در DOMDocument)
    $dom = new DOMDocument('1.0', 'UTF-8');
    libxml_use_internal_errors(true);
    $dom->loadHTML('<?xml encoding="UTF-8">' . $html);
    libxml_clear_errors();
    $xp = new DOMXPath($dom);

    // ---- متادیتا ----
    $meta = fn(string $sel) => ($n = $xp->query($sel)->item(0)) ? trim($n->nodeValue) : '';
    $ogimg = $meta('//meta[@property="og:image"]/@content') ?: $meta('//meta[@name="twitter:image"]/@content');
    $title = $meta('//meta[@property="og:title"]/@content') ?: trim(str_replace(['– انیمکس', '- انیمکس', '– انیمکس'], '', $meta('//title')));
    $desc  = $meta('//meta[@property="og:description"]/@content') ?: $meta('//meta[@name="description"]/@content');

    // استخراج فیلدها از متن صفحه
    $text = trim(preg_replace('/\s+/u', ' ', $dom->textContent));
    $field = function (string $label) use ($text): string {
        if (preg_match('#' . preg_quote($label, '#') . '\s*[:：]?\s*([^•|◆▪■\n]{1,120})#u', $text, $m)) return trim($m[1]);
        return '';
    };
    $year   = $field('سال انتشار') ?: $field('سال');
    $genres = $field('ژانر') ?: $field('انواع');
    $status = $field('وضعیت');
    $rate   = $field('امتیاز') ?: $field('مال');
    $country= $field('کشور');
    $age    = $field('رده سنی') ?: $field('محدوده سنی');

    // ---- لینک‌های دانلود ----
    $links = ax_extract_links($dom, $xp);

    return [
        'ok'   => true,
        'url'  => $url,
        'title'=> $title ?: parse_url($url, PHP_URL_PATH),
        'poster'=> $ogimg,
        'description'=> $desc,
        'meta' => ['year' => $year, 'genres' => $genres, 'status' => $status, 'rate' => $rate, 'country' => $country, 'age' => $age],
        'groups' => $links['groups'],
        'count' => $links['count'],
        'debug_context' => $links['debug'] ?? [],
    ];
}

function ax_is_dl_link(string $href): bool {
    $h = strtolower($href);
    if (strpos($h, 'hollowofthealley.space') !== false) return true;
    if (strpos($h, 'animexstream.fun') !== false) return true;
    if (strpos($h, '?dir=') !== false) return true;
    if (preg_match('#\.(mkv|mp4|avi|zip|rar|m4v|ts|mov|x265)(?:\?|#|$)#i', $h)) return true;
    return false;
}
function ax_text_of(DOMNode $n): string { return trim(preg_replace('/\s+/u', ' ', $n->textContent)); }

// جمع‌آوری متنِ قبل از یک گره (برای پیدا کردن کیفیت/فصل/زیرنویس)
function ax_preceding(DOMNode $node, int $max = 800): string {
    $out = ''; $n = $node; $depth = 0;
    while ($n && mb_strlen($out) < $max && $depth < 6) {
        $p = $n->previousSibling;
        while ($p && mb_strlen($out) < $max) {
            $t = ax_text_of($p);
            if ($t !== '') $out = $t . "\n" . $out;
            $p = $p->previousSibling;
        }
        $n = $n->parentNode; $depth++;
        if (!$n || strtoupper($n->nodeName) === 'BODY') break;
    }
    return $out;
}

function ax_extract_links(DOMDocument $dom, DOMXPath $xp): array {
    $anchors = $xp->query('//a[@href]');
    $items = [];
    $debug = [];
    foreach ($anchors as $a) {
        /** @var DOMElement $a */
        $href = $a->getAttribute('href');
        if (!ax_is_dl_link($href)) continue;
        $role = (strpos(ax_text_of($a), 'پخش') !== false || stripos(ax_text_of($a), 'online') !== false) ? 'stream' : 'download';

                $ctx = ax_preceding($a);
        $STOPS = '(?:دانلود با کیفیت|دانلود فصل|دانلود نسخه|دانلود زیرنویس|دانلود قسمت|پخش آنلاین)';
        // گروه (نزدیک‌ترین هدر): فصل / نسخه / زیرنویس / قسمت
        $group = '';
        if (preg_match_all('#(دانلود فصل|دانلود نسخه|دانلود زیرنویس|دانلود قسمت)\s*((?:(?!' . $STOPS . ')[^\n])*)#u', $ctx, $mg, PREG_SET_ORDER)) {
            $lg = end($mg); $group = trim($lg[1] . ($lg[2] !== '' ? ' ' . $lg[2] : ''));
        }
        $isSub = ($group !== '' && mb_strpos($group, 'دانلود زیرنویس') === 0);
        // کیفیت (نزدیک‌ترین)
        $quality = '';
        if (!$isSub && preg_match_all('#دانلود با کیفیت\s*((?:(?!' . $STOPS . ')[^\n])*)#u', $ctx, $mq, PREG_SET_ORDER)) {
            $quality = trim(end($mq)[1]);
        }
        // نوع زیرنویس (نزدیک‌ترین)
        $sub = '';
        if (!$isSub && preg_match_all('#(زیرنویس چسبیده|سافت\s*ساب|دوبله فارسی|فاقد زیرنویس)\s*((?:(?!' . $STOPS . ')[^\n])*)#u', $ctx, $ms2, PREG_SET_ORDER)) {
            $ls = end($ms2); $sub = trim($ls[1] . ($ls[2] !== '' ? ' ' . $ls[2] : ''));
        }
        // سایز
        $size = '';
        if (preg_match('#(\d+(?:\.\d+)?\s*(?:GB|MB|TB))#ui', $ctx, $mz)) $size = strtoupper(preg_replace('/\s+/', '', $mz[1]));

        if ($isSub) {
            $quality = 'فایل زیرنویس';
        } else {
            if ($group !== '' && $sub !== '' && mb_strpos($group, $sub) === false) $group = $group . ' • ' . $sub;
            if ($quality === '') $quality = ($role === 'stream') ? 'پخش آنلاین' : 'لینک دانلود';
        }
        if ($group === '') $group = 'دانلود';

        $items[] = compact('role', 'href', 'group', 'quality', 'size', 'sub');
        if (count($debug) < 8) $debug[] = ['group' => $group, 'quality' => $quality, 'size' => $size, 'href' => $href];
    }
    // ساخت درخت گروه → کیفیت → لینک‌ها
    $tree = [];
    foreach ($items as $it) {
        $g = $it['group']; $q = $it['quality'];
        $tree[$g][$q] = $tree[$g][$q] ?? ['size' => $it['size'], 'download' => null, 'stream' => null];
        if ($it['role'] === 'download' && !$tree[$g][$q]['download']) $tree[$g][$q]['download'] = $it['href'];
        if ($it['role'] === 'stream' && !$tree[$g][$q]['stream']) $tree[$g][$q]['stream'] = $it['href'];
        if ($it['size'] && !$tree[$g][$q]['size']) $tree[$g][$q]['size'] = $it['size'];
    }
    return ['groups' => $tree, 'count' => count($items), 'debug' => $debug];
}

// بسط پوشه‌های دانلود (?dir=) به لینک‌های تک‌قسمت (best-effort)
function ax_expand_dir(string $url): array {
    $html = ax_http($url, false, 12);
    if (!$html) return [];
    if (preg_match('#application/json#i', substr($html, 0, 200)) && $json = json_decode($html, true)) {
        $out = [];
        foreach (($json['files'] ?? $json['items'] ?? $json) as $f) {
            if (is_array($f) && isset($f['name'], $f['url'])) $out[] = ['name' => $f['name'], 'url' => $f['url']];
        }
        return array_slice($out, 0, 200);
    }
    $d = new DOMDocument('1.0', 'UTF-8');
    libxml_use_internal_errors(true);
    $d->loadHTML('<?xml encoding="UTF-8">' . $html);
    libxml_clear_errors();
    $xp = new DOMXPath($d);
    $out = [];
    foreach ($xp->query('//a[@href]') as $a) {
        $h = $a->getAttribute('href');
        $t = trim($a->textContent);
        if ($t === '' || in_array($t, ['..', '.', 'Parent Directory'], true)) continue;
        if (preg_match('#\.(mkv|mp4|m4v|avi|zip|ts|mov)(?:\?|$)#i', $h)) {
            $out[] = ['name' => $t, 'url' => strpos($h, 'http') === 0 ? $h : rtrim($url, '/') . '/' . ltrim($h, '/')];
        }
    }
    return array_slice($out, 0, 200);
}

/* ============================ 5) پیام‌سازی ================================ */
function ax_caption(array $s): string {
    $m = $s['meta'];
    $lines = [];
    $lines[] = '🎬 <b>' . htmlspecialchars($s['title']) . '</b>';
    if ($m['year'])    $lines[] = "📅 سال انتشار: {$m['year']}";
    if ($m['genres'])  $lines[] = "🎭 ژانر: {$m['genres']}";
    if ($m['rate'])    $lines[] = "⭐ امتیاز: {$m['rate']}";
    if ($m['status'])  $lines[] = "📌 وضعیت: {$m['status']}";
    if ($m['country']) $lines[] = "🌍 کشور: {$m['country']}";
    if ($m['age'])     $lines[] = "🔞 رده سنی: {$m['age']}";
    $lines[] = "🔗 تعداد لینک استخراج‌شده: <b>{$s['count']}</b>";
    $desc = trim($s['description']);
    if ($desc !== '') {
        $remain = 950 - strlen(implode("\n", $lines));
        if ($remain > 40) {
            $lines[] = '';
            $lines[] = '📝 ' . mb_substr($desc, 0, max(60, $remain));
        }
    }
    return implode("\n", $lines);
}

function ax_build_txt(array $s): string {
    $m = $s['meta'];
    $t = [];
    $bar = str_repeat('═', 48);
    $t[] = $bar;
    $t[] = '  ' . $s['title'];
    $t[] = $bar;
    if ($m['year'])    $t[] = "سال انتشار : " . $m['year'];
    if ($m['genres'])  $t[] = "ژانر : " . $m['genres'];
    if ($m['rate'])    $t[] = "امتیاز : " . $m['rate'];
    if ($m['status'])  $t[] = "وضعیت : " . $m['status'];
    if ($m['country']) $t[] = "کشور : " . $m['country'];
    if ($m['age'])     $t[] = "رده سنی : " . $m['age'];
    $t[] = "منبع : " . $s['url'];
    $t[] = '';
    foreach ($s['groups'] as $group => $quals) {
        $t[] = str_repeat('─', 48);
        $t[] = "📁 " . $group;
        foreach ($quals as $q => $info) {
            $sz = $info['size'] ? "  [{$info['size']}]" : '';
            $t[] = "    ▪ کیفیت {$q}{$sz}";
            if ($info['download']) {
                $t[] = "        ▶ دانلود : " . $info['download'];
                // اگر پوشه بود، تک‌قسمت‌ها را هم بیاور
                if (strpos($info['download'], '?dir=') !== false) {
                    $eps = ax_expand_dir($info['download']);
                    if ($eps) {
                        foreach ($eps as $ep) $t[] = "            • {$ep['name']} : {$ep['url']}";
                    }
                }
            }
            if ($info['stream']) $t[] = "        ▶ پخش آنلاین : " . $info['stream'];
        }
    }
    $t[] = '';

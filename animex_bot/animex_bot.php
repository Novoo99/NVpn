function ax_handle(): void {
    $raw = file_get_contents('php://input');
    $u = json_decode($raw, true);
    if (!is_array($u)) return;

    if (isset($u['callback_query'])) {
        $cq = $u['callback_query'];
        $uid = (int)$cq['from']['id'];
        $chat = (int)$cq['message']['chat']['id'];
        $data = $cq['data'];
        ax_store()->ensure_user($uid, $cq['from']['first_name'] ?? '', $cq['from']['username'] ?? '');
        if (strpos($data, 'dl|') === 0) {
            ax_answer($cq['id'], '⏳ در حال ساخت فایل ...');
            ax_send_links_file($chat, $uid, substr($data, 3));
        } elseif ($data === 'upanel') { ax_user_panel($chat, $uid); ax_answer($cq['id']); }
        elseif ($data === 'help') { ax_answer($cq['id']); ax_help($chat); }
        return;
    }

    $msg = $u['message'] ?? null;
    if (!$msg) return;
    $uid = (int)$msg['from']['id'];
    $chat = (int)$msg['chat']['id'];
    $text = trim($msg['text'] ?? '');
    ax_store()->ensure_user($uid, $msg['from']['first_name'] ?? '', $msg['from']['username'] ?? '');

    // claim ownership
    if (strpos($text, '/claim') === 0) {
        $cfg = ax_cfg(); $code = trim(substr($text, 6));
        if ($code !== '' && hash_equals((string)$cfg['owner_code'], $code)) {
            ax_store()->set_admin($uid, true);
            $cfg['owner_id'] = $uid; ax_cfg($cfg);
            ax_send($chat, "🎉 شما مالک/ادمین ارشد ربات شدید!\nاکنون می‌توانید از دستورات ادمین استفاده کنید.\nکد شناسه شما: <code>$uid</code>");
        } else ax_send($chat, "❌ کد نامعتبر است.");
        return;
    }

    if (strpos($text, '/') === 0) {
        $cmd = strtolower(explode(' ', $text)[0]);
        if (ax_admin_dispatch($chat, $uid, $text)) return;
        switch ($cmd) {
            case '/start':
                $u0 = ax_store()->get_user($uid);
                ax_send($chat,
                    "👋 سلام " . htmlspecialchars($msg['from']['first_name'] ?? '') . "!\n\n"
                    . "🎬 <b>ربات لیچر انیمکس</b>\n"
                    . "کافیست <b>لینک صفحه انیمه/فیلم/سریال</b> از سایت animex.click را بفرستید تا:\n"
                    . "  • پوستر + اطلاعات (سال، ژانر، امتیاز، توضیحات) دریافت کنید\n"
                    . "  • با زدن دکمه «مشاهده لینک‌های دانلود»، همه لینک‌ها را به‌صورت <b>مرتب و فایل‌شده</b> تحویل بگیرید\n\n"
                    . "📊 سقف روزانه: <b>" . (ax_cfg()['daily_limit'] ?? 500) . "</b> درخواست",
                    ['inline_keyboard' => [[['text' => '👤 پنل کاربری', 'callback_data' => 'upanel']]]]);
                break;
            case '/panel': case '/myaccount': ax_user_panel($chat, $uid); break;
            case '/id': ax_send($chat, "🆔 شناسه شما: <code>$uid</code>"); break;
            case '/help': case '/start_help': ax_help($chat); break;
            default:
                ax_send($chat, "❓ دستور نامشخص. /help را ببینید یا یک لینک animex بفرستید.");
        }
        return;
    }

    if (preg_match('#https?://[^\s]+#i', $text, $mm)) { ax_process_url($chat, $uid, $mm[0]); return; }
    ax_send($chat, "🔗 لطفاً یک <b>لینک معتبر</b> از animex.click بفرستید.\nمثال: <code>https://animex.click/anime/...</code>");
}

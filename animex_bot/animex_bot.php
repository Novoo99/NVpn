function ax_is_admin(int $uid): bool {
    $u = ax_store()->get_user($uid);
    return $u && (int)$u['is_admin'] === 1;
}
function ax_admin_dispatch(int $chat, int $uid, string $text): bool {
    if (!ax_is_admin($uid)) return false;
    $parts = preg_split('/\s+/', trim($text));
    $cmd = strtolower(ltrim($parts[0] ?? '', '/'));
    switch ($cmd) {
        case 'setvip':
            $id = (int)($parts[1] ?? 0); $days = (int)($parts[2] ?? 0);
            if ($id <= 0 || $days <= 0) { ax_send($chat, "❗ کاربرد: /setvip <id> <days>"); break; }
            ax_store()->ensure_user($id, '-', '-');
            ax_store()->set_vip($id, $days);
            ax_send($chat, "✅ VIP به <code>$id</code> برای <b>$days</b> روز اعمال شد."); break;
        case 'ban':
            $id = (int)($parts[1] ?? 0);
            if ($id <= 0) { ax_send($chat, "❗ کاربرد: /ban <id> [reason]"); break; }
            $reason = trim(implode(' ', array_slice($parts, 2)));
            ax_store()->ensure_user($id, '-', '-');
            ax_store()->ban($id, $reason);
            ax_send($chat, "🚫 کاربر <code>$id</code> مسدود شد."); break;
        case 'unban':
            $id = (int)($parts[1] ?? 0);
            if ($id <= 0) { ax_send($chat, "❗ کاربرد: /unban <id>"); break; }
            ax_store()->unban($id); ax_send($chat, "✅ کاربر <code>$id</code> آزاد شد."); break;
        case 'addadmin':
            $id = (int)($parts[1] ?? 0);
            if ($id <= 0) { ax_send($chat, "❗ کاربرد: /addadmin <id>"); break; }
            ax_store()->ensure_user($id, '-', '-');
            ax_store()->set_admin($id, true); ax_send($chat, "✅ <code>$id</code> ادمین شد."); break;
        case 'deladmin':
            $id = (int)($parts[1] ?? 0);
            ax_store()->set_admin($id, false); ax_send($chat, "✅ <code>$id</code> از ادمینی برداشته شد."); break;
        case 'userinfo':
            $id = (int)($parts[1] ?? 0); $u = ax_store()->get_user($id);
            if (!$u) { ax_send($chat, "❌ کاربر یافت نشد."); break; }
            ax_send($chat, "👤 اطلاعات کاربر <code>$id</code>:\n" . json_encode($u, JSON_PRETTY_PRINT | JSON_UNESCAPED_UNICODE)); break;
        case 'setlimit':
            $n = (int)($parts[1] ?? -1); if ($n < 0) { ax_send($chat, "❗ کاربرد: /setlimit <n>"); break; }
            $cfg = ax_cfg(); $cfg['daily_limit'] = $n; ax_cfg($cfg);
            ax_send($chat, "✅ سقف روزانه به <b>$n</b> تغییر کرد."); break;
        case 'stats':
            ax_send($chat, "📊 <b>آمار ربات</b>\n" . json_encode(ax_store()->stats(), JSON_PRETTY_PRINT | JSON_UNESCAPED_UNICODE | JSON_UNESCAPED_SLASHES)); break;
        case 'broadcast':
            $msg = trim(substr($text, strpos($text, ' ')));
            if ($msg === '') { ax_send($chat, "❗ کاربرد: /broadcast <text>"); break; }
            $ids = ax_store()->all_ids(); $ok = 0;
            foreach ($ids as $to) { $r = ax_send($to, $msg); if (!empty($r['ok'])) $ok++; }
            ax_send($chat, "✅ پیام به $ok کاربر ارسال شد."); break;
        default: return false;
    }
    return true;
}


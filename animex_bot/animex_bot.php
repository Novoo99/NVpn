function ax_process_url(int $chat, int $uid, string $url): void {
    $u = ax_store()->get_user($uid);
    if ($u && (int)$u['banned'] === 1) { ax_send($chat, "🚫 شما مسدود شده‌اید.\nدلیل: " . ($u['ban_reason'] ?: '—')); return; }
    $cfg = ax_cfg();
    $limit = (int)($cfg['daily_limit'] ?? 500);
    $isVip = $u && (int)$u['vip_until'] > time();
    if (!$isVip) {
        $used = ax_store()->usage_today($uid);
        if ($used >= $limit) {
            $reset = strtotime('tomorrow 00:00') - time();
            $h = floor($reset / 3600);
            ax_send($chat, "⛔ سقف روزانه شما ({<b>$limit</b>} درخواست) پر شده است.\nبعداً تلاش کنید (حدود {$h} ساعت تا ریست).\n\n💎 برای استفاده نامحدود، اشتراک VIP بگیرید.");
            return;
        }
    }
    if (!preg_match('#^https?://#i', $url)) $url = 'https://' . $url;

    ax_send($chat, "🔍 در حال استخراج اطلاعات از سایت ...");
    $hash = hash('sha1', $url);
    $cached = ax_store()->cache_get($hash);
    $s = $cached ? json_decode($cached['data'], true) : null;
    if (!$s) {
        $s = ax_scrape($url);
        if (!empty($s['ok'])) ax_store()->cache_set($hash, $url, json_encode($s, JSON_UNESCAPED_UNICODE));
    }
    if (empty($s['ok'])) { ax_send($chat, "❌ " . ($s['error'] ?? 'خطا در استخراج. لینک را بررسی کنید.')); return; }

    ax_store()->inc_usage($uid);
    ax_store()->log($uid, 'scrape', $url);

    $caption = ax_caption($s);
    $markup = ['inline_keyboard' => [[['text' => '🎬 مشاهده لینک‌های دانلود', 'callback_data' => 'dl|' . $hash]]]];
    if (!empty($s['poster'])) {
        $r = ax_tg('sendPhoto', [
            'chat_id' => $chat, 'photo' => $s['poster'], 'caption' => $caption,
            'parse_mode' => 'HTML', 'reply_markup' => json_encode($markup, JSON_UNESCAPED_UNICODE)]);
        if (empty($r['ok'])) { // fallback: دانلود پوستر و آپلود
            $bin = ax_http($s['poster'], true, 15);
            if ($bin) {
                $tmp = tempnam(sys_get_temp_dir(), 'px') . '.jpg';
                file_put_contents($tmp, $bin);
                ax_tg_upload('sendPhoto', [
                    'chat_id' => $chat, 'caption' => $caption, 'parse_mode' => 'HTML',
                    'reply_markup' => json_encode($markup, JSON_UNESCAPED_UNICODE)], ['photo' => $tmp]);
                @unlink($tmp);
            } else {
                ax_send($chat, $caption, $markup);
            }
        }
    } else {
        ax_send($chat, $caption, $markup);
    }
}

